#include <stdio.h>

/*
Data Types in C
    - int
    - float
    - double
    - char

and way of printing them

int - print with %d
float - print with %f
double - print with %lf
char - print with %c

 */

int main()
{
    int a = 10;
    float b = 10.5;
    double c = 10.5;
    char d = 'A';

    printf("int a = %d\n", a);
    printf("float b = %f\n", b);
    printf("double c = %lf\n", c);
    printf("char d = %c\n", d);

    return 0;
}